                                    <p>GLPI (acronyme de gestionnaire libre de parc informatique) est un logiciel libre de gestion des services informatiques (ITSM) et de gestion des services d’assistance(issue tracking system et ServiceDesk). Cette solution libre est éditée en PHP et distribuée sous licence GPL.

                                        En tant que technologie libre, toute personne peut exécuter, modifier ou développer le code qui est libre. De ce fait, les contributeurs peuvent participer à l’évolution du logiciel en soumettant des modules supplémentaires libre et open source, sur github.

                                    GLPI est une application web qui aide les entreprises à gérer leur système d’information. Parmi ses caractéristiques, cette solution est capable de construire un inventaire de toutes les ressources de la société et de réaliser la gestion des tâches administratives et financières. Les fonctionnalités de cette solution aident les Administrateurs IT à créer une base de données regroupant des ressources techniques et de gestion, ainsi qu’un historique des actions de maintenance. La fonctionnalité de gestion d’assistance ou helpdesk fournit aux utilisateurs un service leur permettant de signaler des incidents ou de créer des demandes basées sur un actif ou non, ceci par la création d’un ticket d’assistance qui est géré par un technicien .</p>
